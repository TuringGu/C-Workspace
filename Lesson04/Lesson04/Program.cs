using System;

namespace Lesson04
{
	class MainClass
	{
		public static void Main (string[] args)
		{
			// Declare an int variable level
			int level;

			// Initializing variables
			level = 6;
			Console.WriteLine (level);

			// Declare a float variable exp
			float exp;

			// Initializing variables
			exp = 0.6f;
			Console.WriteLine (exp);

			// Declare an int variable str
			string str;

			// Initializing variables
			str = "你好老王";
			Console.WriteLine (str);

			// Declaring and initializing int constant
			const int a = 9;
			Console.WriteLine (a);

			// Circumference ratio
			const float pi = 3.1415926f;
			Console.WriteLine (pi);
		}
	}
}
